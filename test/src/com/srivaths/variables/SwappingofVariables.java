package com.srivaths.variables;

public class SwappingofVariables {
public static void main(String[] args) {
	

	int a = 891997;
			int b = 221998;
int temp;
  System.out.println("Before Swapping");
  System.out.println("a="+ a + "b="+ b);
  
  temp=a;
  a=b;
		  b=temp;
		  System.out.println("After Swapping");
		  System.out.println("a="+ a + "b=" + b);
}
}
